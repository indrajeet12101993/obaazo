package com.ansh.obaazo.model;

public class UserDetails {
    private String id;
    private String oauth_provider;
    private String oauth_uid;
    private String name;
    private String last_name;
    private String email;
    private String mobile;
    private String gender;
    private String locale;
    private String picture_url;
    private String profile_url;
    private String created;
    private String modified;
    private String otp;
    private String social_login;
    private String gstid;
    private String company_name;
    private String company_address;
    private String picture;
    public String getId() { return this.id; }

    public void setId(String id) { this.id = id; }

    public String getOauthProvider() { return this.oauth_provider; }

    public void setOauthProvider(String oauth_provider) { this.oauth_provider = oauth_provider; }



    public String getOauthUid() { return this.oauth_uid; }

    public void setOauthUid(String oauth_uid) { this.oauth_uid = oauth_uid; }



    public String getName() { return this.name; }

    public void setName(String name) { this.name = name; }



    public String getLastName() { return this.last_name; }

    public void setLastName(String last_name) { this.last_name = last_name; }



    public String getEmail() { return this.email; }

    public void setEmail(String email) { this.email = email; }



    public String getMobile() { return this.mobile; }

    public void setMobile(String mobile) { this.mobile = mobile; }



    public String getGender() { return this.gender; }

    public void setGender(String gender) { this.gender = gender; }


    public String getLocale() { return this.locale; }

    public void setLocale(String locale) { this.locale = locale; }



    public String getPictureUrl() { return this.picture_url; }

    public void setPictureUrl(String picture_url) { this.picture_url = picture_url; }



    public String getProfileUrl() { return this.profile_url; }

    public void setProfileUrl(String profile_url) { this.profile_url = profile_url; }



    public String getCreated() { return this.created; }

    public void setCreated(String created) { this.created = created; }



    public String getModified() { return this.modified; }

    public void setModified(String modified) { this.modified = modified; }



    public String getOtp() { return this.otp; }

    public void setOtp(String otp) { this.otp = otp; }



    public String getSocialLogin() { return this.social_login; }

    public void setSocialLogin(String social_login) { this.social_login = social_login; }



    public String getGstid() { return this.gstid; }

    public void setGstid(String gstid) { this.gstid = gstid; }



    public String getCompanyName() { return this.company_name; }

    public void setCompanyName(String company_name) { this.company_name = company_name; }



    public String getCompanyAddress() { return this.company_address; }

    public void setCompanyAddress(String company_address) { this.company_address = company_address; }



    public String getPicture() { return this.picture; }

    public void setPicture(String picture) { this.picture = picture; }
}
